import express from "express";
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import User from "../models/User.js";
import { Op } from "sequelize";
import { authenticateToken } from "../middleware/auth.js";

if (!process.env.JWT_SECRET) {
	console.warn(
		"Warning: JWT_SECRET is not set in environment variables. Using default secret key."
	);
}

const JWT_SECRET = process.env.JWT_SECRET || "yarn-track-secret-key-2024";

const router = express.Router();

// Register
router.post("/register", async (req, res) => {
	try {
		const { username, email, password } = req.body;

		// Check if user already exists
		const existingUser = await User.findOne({
			where: {
				[Op.or]: [{ email }, { username }],
			},
		});

		if (existingUser) {
			return res.status(400).json({ error: "User already exists" });
		}

		// Hash password
		const hashedPassword = await bcrypt.hash(password, 10);

		// Create user
		const user = await User.create({
			username,
			email,
			password: hashedPassword,
		});

		// Generate token
		const token = jwt.sign(
			{
				id: user.id,
				username: user.username,
				email: user.email,
				role: user.role,
			},
			JWT_SECRET,
			{ expiresIn: "24h" }
		);

		res.status(201).json({
			user: {
				id: user.id,
				username: user.username,
				email: user.email,
				role: user.role,
			},
			token,
		});
	} catch (error) {
		console.error("Registration error:", error);
		res.status(500).json({ error: "Failed to register user" });
	}
});

// Login
router.post("/login", async (req, res) => {
	try {
		const { username, password } = req.body;

		// Find user
		const user = await User.findOne({
			where: {
				[Op.or]: [{ username }, { email: username }],
			},
		});

		if (!user) {
			return res.status(401).json({ error: "Invalid credentials" });
		}

		// Check password
		const isMatch = await bcrypt.compare(password, user.password);

		if (!isMatch) {
			return res.status(401).json({ error: "Invalid credentials" });
		}

		// Generate token
		const token = jwt.sign(
			{
				id: user.id,
				username: user.username,
				email: user.email,
				role: user.role,
			},
			JWT_SECRET,
			{ expiresIn: "24h" }
		);

		res.json({
			user: {
				id: user.id,
				username: user.username,
				email: user.email,
				role: user.role,
			},
			token,
		});
	} catch (error) {
		console.error("Login error:", error);
		res.status(500).json({ error: "Failed to login" });
	}
});

// Get current user
router.get("/me", async (req, res) => {
	try {
		const token = req.header("Authorization")?.replace("Bearer ", "");

		if (!token) {
			return res.status(401).json({ error: "Please authenticate" });
		}

		const decoded = jwt.verify(token, JWT_SECRET);

		const user = await User.findByPk(decoded.id, {
			attributes: ["id", "username", "email", "role"],
		});

		if (!user) {
			return res.status(404).json({ error: "User not found" });
		}

		res.json(user);
	} catch (error) {
		console.error("Get user error:", error);
		res.status(500).json({ error: "Failed to get user" });
	}
});

// Get all users with sales role
router.get("/sales-users", authenticateToken, async (req, res) => {
	try {
		// Find all users with sales role
		const salesUsers = await User.findAll({
			where: { role: "sales" },
			attributes: ["id", "username", "email"],
			order: [["username", "ASC"]],
		});

		res.json(salesUsers);
	} catch (error) {
		console.error("Error fetching sales users:", error);
		res.status(500).json({ error: "Failed to fetch sales users" });
	}
});

export default router;
